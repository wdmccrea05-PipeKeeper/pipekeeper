function countKeys(obj, prefix = '') {
  let count = 0;
  for (const key in obj) {
    const val = obj[key];
    if (typeof val === 'object' && val !== null && !Array.isArray(val)) {
      count += countKeys(val, `${prefix}${key}.`);
    } else {
      count++;
    }
  }
  return count;
}

